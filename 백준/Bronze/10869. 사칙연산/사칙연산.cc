#include <stdio.h>

int main(void){

    int first, second;
    scanf("%d %d", &first, &second);

    printf("%d\n", first + second);
    printf("%d\n", first - second);
    printf("%d\n", first * second);
    printf("%d\n", first / second);
    printf("%d\n", first % second);

    return 0;

}